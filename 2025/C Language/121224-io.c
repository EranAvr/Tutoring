#include <stdio.h>

int main(){
    // int:
    int input;

    scanf("%d\n", &input);

    printf("User int value = %d \n", input);

    // char:
    char c;
    scanf("%c\n", &c);
    printf("User char value = %c\n", c);

    // float:
    float f;
    scanf("%f", &f);
    printf("User float value = %f\n", f);



    return 0;
}