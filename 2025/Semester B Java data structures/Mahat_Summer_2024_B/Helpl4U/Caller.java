package Mahat_Summer_2024_B.Helpl4U;

public class Caller {
    private String name;
    private String phone;

    public Caller(String name, String phone) {
        this.name = name;
        this.phone = phone;
    }

    public String getName() {
        return name;
    }
    public String getPhone() {
        return phone;
    }
}
